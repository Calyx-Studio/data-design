		<h1>Use Case & Interaction</h1>
<h2>Use Case</h2>
		<h3>Scenario</h3>
		<p>Mia logs into Netflix and begins by searching for educational content from the search icon. She finds and selects 3 age appropriate, education shows and adds to "My List". Mia clicks on the first show to begin playing shows in sequence.</p>
		<h3>Precondition</h3>
		<p>Mia needs to be logged into Netflix and logged into "kids" profile.</p>
		<h3>Post condition</h3>
		<p>Mia has added educational shows to her "my list"</p>
			<h2>Interaction flow</h2>
					<p>Mia clicks search icon and types "educational" content from the search icon, clicks enter. <br>
			Netflix returns educational content. <br>
			Mia hovers over show clip. <br>
			Netflix returns menu icons on clip including "add to list".<br>
			Mia clicks "add to list".</p>
